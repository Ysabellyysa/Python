# Operadores Aritméticos operações matemáticas entre números

x = 10
y = 3

# Operações básicas:
print(x + y)      # Adição: 10 + 3 = 13
print(x - y)      # Subtração: 10 - 3 = 7
print(x * y)      # Multiplicação: 10 * 3 = 30
print(x / y)      # Divisão: 10 / 3 = 3.333...
print(x % y)      # Módulo (resto): 10 % 3 = 1

# Exemplo com módulo para identificar par/ímpar:
par = 34
impar = 33

print(par % 2)    # 0 (par, pois resto de divisão por 2 é 0)
print(impar % 2)  # 1 (ímpar, pois resto é 1) 
