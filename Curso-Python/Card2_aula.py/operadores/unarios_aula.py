# Operadores unários atuam sobre um único operando

y = 4

# Operador NOT: inverte booleanos
print(not False)  # True (negação de False)
print(not True)   # False (negação de True)

# Operador de negação unária: -
print(-3)         # -3 (número negativo)
print(-y)         # -4 (negação de y, que vale 4)

# Operador unário de adição
print(+3)         # 3 (mantém positivo)

w = 12
print(+w)         # 12 (mantém o sinal de w)
