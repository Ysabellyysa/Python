# Conjuntos coleções não ordenadas que não permitem elementos duplicados.
#{} cria um dicionário vazio para um conjunto vazio use set().

print({1, 2, 3})  # cria um conjunto literal e imprime seus elementos
print(type({1, 2, 3}))  # mostra o tipo do objeto

conj = {1, 2, 3, 4, 5}  # atribui um conjunto à variável conj
print(conj)  # imprime o conteúdo do conjunto
print(type(conj))  # confirma que conj é do tipo set

#elementos duplicados seriam automaticamente removidos ao criar um set.



