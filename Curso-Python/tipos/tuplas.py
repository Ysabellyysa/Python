#Tuplas são sequências ordenadas, o que significa que seus valores 
# não podem ser alterados após a criação. Criadas com parênteses ().

nomes = ('Ana Maria ', 'João', 'Marya', 'José') #tupla com 4 elementos 

print('João' in nomes) #verifica se o valor 'João' está na tupla nomes    

print(nomes[0]) #acessa o primeiro elemento da tupla   
print(nomes[1:3]) #acessa do segundo ao terceiro elemento da tupla 


print(type(nomes)) #mostra o tipo da variável nomes    
print(nomes) #mostra o conteúdo da variável nomes      